package com.example.android.contactlist;

public class SingleContactInfo {
    String name;
    String surname;
    String email;
    String age;

    public SingleContactInfo() {
    }

    public SingleContactInfo(String name, String surname, String email, String age) {
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.age = age;
    }
}
